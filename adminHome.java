    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branchName=(String)jComboBox2.getSelectedItem();
        String rollno=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String motherName=jTextField3.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","sayali");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,motherName)values('"+rollno+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+motherName+"')");
            JOptionPane.showMessageDialog(null,"Data Saved Sucessfully!");
            setVisible(false);
            new adminHome().setVisible(true);
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

